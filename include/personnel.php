            <h1>Le personnel de la M2L</h1>
            <p class="lead"> Interview de Mr Sapin : <br/><br/>

Q.	Pouvez-vous nous parler du personnel de la M2L et de leurs missions ?<br/><br/>

R.	Les personnes qui travaillent ici sont de différentes catégories. Il y a d’abord ceux que j’appelle les «professionnels». Il y a deux employés du Conseil Régional (des factotums). Il y a sept ETP (Équivalents Temps Plein) employés par le CROSL ; il y a le personnel des prestataires (gardiennage, ménage) et les salariés des ligues. Ensuite, il y a les bénévoles qui sont les élus des associations : CROSL, CDOS, ligues, comités départementaux. Il y a également les élus du Conseil Régional et enfin tous les autres : visiteurs, stagiaires, etc.<br/><br/>

Q.	Quelles sont les missions des personnels « Région » et « CROSL » ?<br/><br/>

R.	Nous avons 2,25 ETP « région », les deux factotums dont je vous parlais précédemment, et moi, qui suis employé pour un quart de mon poste, par le conseil régional. Cela s’explique par le fait que je ne pourrais avoir logiquement d’autorité sur les personnels « Région » si je n’en fais pas partie moi-même.<br/><br/>

Les autres ETP du CROSL sont les suivants :<br/><br/>
-	0,75 ETP pour le poste de direction, comme je viens de vous dire<br/>
-	0,8 ETP sur le suivi du Plan Impact Emploi Association<br/>
-	0,5 ETP sur la gestion : facturation du CROSL et du Conseil Régional, comptabilité, gestion des réservations<br/>
-	1 ETP sur la formation, la communication et le secrétariat<br/>
-	0,80 ETP sur le domaine « SAPHIR », c’est-à-dire les aspects médicaux liés aux mouvements sportifs<br/>
-	1 ETP d’infographiste et gestion des photocopieuses numériques.<br/>
-	1 ETP sur l’accueil qui va bientôt passer en deux fois 0,5 ETP.<br/>
