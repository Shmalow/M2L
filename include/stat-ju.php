            <h1>Le statut juridique</h1>
            <p class="lead"> Interview de Mr Sapin : <br/><br/>

            	Q.	M. Sapin, merci de nous accueillir dans ces locaux tous neufs et superbes de l’extension de la Maison des Ligues de Lorraine. Pouvez-vous nous dire quel est le statut juridique de cette maison, et quelles sont ses sources de financement ?<br/><br/>

				R.	La Maison des Ligues est un établissement du Conseil Régional. Ce n’est pas une entité juridique en propre. Elle est financée à 100 % (pour son fonctionnement et pour la construction récente de l’extension des bâtiments C et D) par le Conseil Régional et sans aucune participation du Conseil Général de Meurthe et Moselle, bien qu’elle abrite un certain nombre de comités départementaux. Une convention de cogestion a été passée entre le Conseil Régional et le Comité Régional Olympique et Sportif de Lorraine pour la gestion de l’outil « Maison des Ligues ». Le CROSL est une association financée par le ministère via le CNDS (Centre National de Développement du Sport).<br/><br/>

